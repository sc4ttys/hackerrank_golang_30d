class Node:
    def __init__(self,data):
        self.data = data
        self.next = None 
class Solution: 
    def insert(self,head,data):
            p = Node(data)           
            if head==None:
                head=p
            elif head.next==None:
                head.next=p
            else:
                start=head
                while(start.next!=None):
                    start=start.next
                start.next=p
            return head  
    def display(self,head):
        current = head
        while current:
            print(current.data,end=' ')
            current = current.next

    def removeDuplicates(self,head):
        #Write your code here
        if head == None or head.next == None:
            return head
        else: 
            curr = head
            while(curr.next != None):
                next_node = curr.next
                if curr.data == next_node.data:
                    curr.next = next_node.next
                    continue
                curr = curr.next
            return head 
                
mylist= Solution()
T=int(input())
